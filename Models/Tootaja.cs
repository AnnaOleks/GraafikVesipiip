/* Этот файл описывает одного работника кальянной в приложении.
 * Каждый объект Tootaja — это одна запись (строка) в таблице Tootaja базы данных SQLite.*/

namespace GraafikVesipiip.Models
{
    public class Tootaja
    {
        [SQLite.PrimaryKey, SQLite.AutoIncrement] // первичный ключ, автоинкремент
        /*[SQLite.PrimaryKey] — обозначает, что это уникальный идентификатор строки (аналог PRIMARY KEY в SQL).
         * [SQLite.AutoIncrement] — при добавлении нового сотрудника значение Id создаётся автоматически (1, 2, 3, …).*/

        public int Id { get; set; } // int Id { get; set; } — само свойство, хранящее этот номер.
        public string Nimi { get; set; } = "";    // имя сотрудника (обязательное поле)
        /*Nimi — обязательное текстовое поле.
         * Тип string без ? — значит, не допускает null.
         * = "" — начальное значение по умолчанию, чтобы не было NullReferenceException, если кто-то случайно не присвоит имя.*/
        public string? Telefon { get; set; }   // номер телефона (может быть null)
        public string? FotoTee { get; set; }      // путь к фото (может быть null)
        /*FotoTee (буквально «путь к фото») — строка, где хранится путь к файлу (например, "images/marta.jpg").
         * string? — допускает null, то есть фотография может отсутствовать.
         * Это не сама картинка, а ссылка на файл или локальный путь на устройстве.*/
        public string? VarvHex { get; set; }      // цвет для метки/аватарки в формате "#RRGGBB" (опционально)
        /*VarvHex (по-эстонски «цвет») — хранит код цвета в формате #RRGGBB или #AARRGGBB.
         * Используется для окрашивания фона карточки, иконки или имени сотрудника в интерфейсе.
         * Опционально (?) — можно оставить пустым, если цвет не задан.*/
    }
}

